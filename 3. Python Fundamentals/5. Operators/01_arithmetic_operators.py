

a = 5

b = 3 

# Arithmetic Operator 
print("a + b = ", a + b)
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = ", a / b) # Division	5 / 2	2.5
print("a % b = ", a % b)
print("a // b = ", a // b) # Floor Division	5 // 2 = 2 -> ignore the decimal points
print("a ** b = ", a ** b) # Exponentiation	2 ** 3 = 8 -> a ^ b
